import 'package:flutter/material.dart';
import 'package:flutter_screenutil/flutter_screenutil.dart';
import 'package:google_fonts/google_fonts.dart';

/// Class to add fonts with specified family and size which are used in the app

class AppFonts {
  // regular font 12 with font weight 400
  static TextStyle regular12w400() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w400, fontSize: 11.sp);

  static TextStyle regular14w400() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w400, fontSize: 13.sp);

  static TextStyle regular16w400() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w400, fontSize: 15.sp);

  static TextStyle regular18w400() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w400, fontSize: 17.sp);

  static TextStyle regular20w400() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w400, fontSize: 19.sp);

  // regular font 12 with font weight 500
  static TextStyle regular12w500() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w500, fontSize: 11.sp);

  static TextStyle regular14w500() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w500, fontSize: 13.sp);

  static TextStyle regular16w500() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w500, fontSize: 15.sp);

  static TextStyle regular18w500() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w500, fontSize: 17.sp);

  static TextStyle regular20w500() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w500, fontSize: 19.sp);

  // regular font 12 with font weight 600
  static TextStyle regular12w600() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w600, fontSize: 11.sp);

  static TextStyle regular14w600() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w600, fontSize: 13.sp);

  static TextStyle regular16w600() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w600, fontSize: 15.sp);

  static TextStyle regular18w600() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w600, fontSize: 17.sp);

  static TextStyle regular20w600() =>
      GoogleFonts.poppins(fontWeight: FontWeight.w600, fontSize: 19.sp);
}
